	<div class="entry">
		<img src="/images/enemies/white_wyvern.png" alt="World Design" class="chapter-image" />
		<div class="text">
			<h3>Pillar 1: A World, Not a Treadmill</h3>
			<p>
				Our core philosophy is <strong>"No Grinding."</strong> Player progression is not tied to defeating
				hundreds of identical monsters. Instead, wild encounters are "Mastery Puzzles," overcome with
				the right gear and stats, not repetitive combat. This respects the player's time and makes every
				encounter a meaningful strategic decision.
			</p>
		</div>
	</div>

	<div class="entry">
		<img src="/game_icons/heart_rank.png" alt="NPC Interaction" class="chapter-image" />
		<div class="text">
			<h3>Pillar 2: A Personalized Narrative</h3>
			<p>
				Your relationships with the characters are central to the experience. Our dual-rank system
				(Sword and Heart) allows you to choose your path: follow the main plot or build personal
				relationships. The Affinity system provides a way to progress through actions like gifting
				and combat, not just dialogue, making the world feel reactive to how you choose to play.
			</p>
		</div>
	</div>

	<div class="entry">
		<img src="/weapons/phobos_and_deimos.png" alt="Item Design" class="chapter-image" />
		<div class="text">
			<h3>Pillar 3: The Road Ahead</h3>
			<p>
				The game is built on a stable foundation, and we have a clear roadmap for our two biggest
				upcoming features: Strategic Turn-Based Combat (with weapon alternation and the "Status &
				Exploit" system) and Deeper NPC Interaction (fully implementing the "Give Item" and
				"Challenge" systems).
			</p>
		</div>
	</div>
